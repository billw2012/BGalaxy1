/*
    Test:  cxsqrt  cxexp   cxexp10  cxexp2   
           cxsin   cxcos   cxsinh   cxcosh   
	   cxasin  cxacos  cxasinh  cxacosh

    Uses:  All the tested functions + atocx  xpr_asprint

    Input file:  data/cxgen.dat
*/
#include<stdio.h>
#include<stdlib.h>
#include"cxpre.h"

#define DECD 32

int
get_line (char *buffer, int size, FILE * fp)
{
  if ((fgets (buffer, size, fp)))
    {
      int i;

      for (i = 0; buffer[i] != '\0'; i++);
      if (i > 0 && buffer[i - 1] == '\n')
	buffer[--i] = '\0';
      return i;
    }
  else
    return -1;
}

typedef struct cxpr (*pfun) (struct cxpr);
extern int xErrNo;

int
main (int na, char **av)
{
  struct cxpr z, w;
  FILE *fp;
  char num[1024], *re, *im;
  const char *nlist[] = {
    "cxsqrt",
    "cxexp", "cxexp10", "cxexp2",
    "cxsin", "cxcos",
    "cxsinh", "cxcosh",
    "cxasin", "cxacos",
    "cxasinh", "cxacosh",
    NULL
  };
  pfun flist[] = {
    cxsqrt,
    cxexp, cxexp10, cxexp2,
    cxsin, cxcos,
    cxsinh, cxcosh,
    cxasin, cxacos,
    cxasinh, cxacosh
  };
  int i;

  if (na != 2)
    {
      printf ("para: input_file\n");
      exit (-1);
    }
  fp = fopen (*++av, "r");
  for (i = 0; nlist[i] != NULL; i++)
    {
      printf ("Test of %s\n", nlist[i]);
      while (get_line (num, 1024, fp) != -1)
	{
	  printf ("*string in : \t%s\n", num);
	  /* convert ascii string to extended precision complex */
	  z = atocx (num);
	  re = xpr_asprint (z.re, 1, 0, DECD);
	  im = xpr_asprint (z.im, 1, 0, DECD);
	  printf (" z =  \t\t%s  \t%s\n", re, im);
	  free ((void *) re);
	  free ((void *) im);
	  printf (" %s(z) = \t", nlist[i]);
	  xErrNo = 0;
	  w = (*flist[i]) (z);
	  switch (xErrNo)
	    {
	    case XENONE:
	      re = xpr_asprint (w.re, 1, 0, DECD);
	      im = xpr_asprint (w.im, 1, 0, DECD);
	      printf ("%s  \t%s\n", re, im);
	      free ((void *) re);
	      free ((void *) im);
	      break;
	    case XEDIV:
	      printf ("*** Division by zero\n");
	      break;
	    case XEDOM:
	      printf ("*** Out of domain\n");
	      break;
	    case XEBADEXP:
	      printf ("*** Bad exponent\n");
	      break;
	    case XFPOFLOW:
	      printf ("*** Floating point overflow\n");
	      break;
	    default:
	      printf ("*** Unknown error\n");
	    }
	}
      printf ("\n");
      rewind (fp);
    }
  fclose (fp);
  return 0;
}

