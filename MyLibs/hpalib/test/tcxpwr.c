/*
    Test:  cxpwr  cxroot

    Uses:  cxpwr  cxroot  strtocx  xpr_asprint

    Input file:  data/cxpwr.dat
*/
#include<stdio.h>
#include<stdlib.h>
#include"cxpre.h"

#define DECD 32

int
get_line (char *buffer, int size, FILE * fp)
{
  if ((fgets (buffer, size, fp)))
    {
      int i;

      for (i = 0; buffer[i] != '\0'; i++);
      if (i > 0 && buffer[i - 1] == '\n')
	buffer[--i] = '\0';
      return i;
    }
  else
    return -1;
}

extern int xErrNo;

int
main (int na, char **av)
{
  struct cxpr z, w;
  FILE *fp;
  char num[1024], *re, *im, *endptr;
  int n, i;

  if (na != 2)
    {
      printf ("para: input_file\n");
      exit (-1);
    }
  fp = fopen (*++av, "r");
  /* Test of cxpwr() */
  printf ("\nTest of cxpwr()\n");
  while (get_line (num, 1024, fp) != -1)
    {
      printf ("*string in : \t%s\n", num);
      /* convert ascii string to extended precision complex */
      z = strtocx (num, &endptr);
      re = xpr_asprint (z.re, 1, 0, DECD);
      im = xpr_asprint (z.im, 1, 0, DECD);
      printf (" z =  \t\t%s  \t%s\n", re, im);
      free ((void *) re);
      free ((void *) im);
      n = atoi (endptr);
      printf (" n = %d\n", n);
      printf (" cxpwr(z,n) = \t");
      xErrNo = 0;
      w = cxpwr (z, n);
      switch (xErrNo)
	{
	case XENONE:
	  re = xpr_asprint (w.re, 1, 0, DECD);
	  im = xpr_asprint (w.im, 1, 0, DECD);
	  printf ("%s  \t%s\n", re, im);
	  free ((void *) re);
	  free ((void *) im);
	  break;
	case XEDIV:
	  printf ("*** Division by zero\n");
	  break;
	case XEDOM:
	  printf ("*** Out of domain\n");
	  break;
	case XEBADEXP:
	  printf ("*** Bad exponent\n");
	  break;
	case XFPOFLOW:
	  printf ("*** Floating point overflow\n");
	  break;
	default:
	  printf ("*** Unknown error\n");
	}
    }
  rewind (fp);
  /* Test of cxroot() */
  printf ("\nTest of cxroot()\n");
  while (get_line (num, 1024, fp) != -1)
    {
      printf ("*string in : \t%s\n", num);
      /* convert ascii string to extended precision complex */
      z = strtocx (num, &endptr);
      re = xpr_asprint (z.re, 1, 0, DECD);
      im = xpr_asprint (z.im, 1, 0, DECD);
      printf (" z =  \t\t%s  \t%s\n", re, im);
      free ((void *) re);
      free ((void *) im);
      n = atoi (endptr);
      printf (" n = %d\n", n);
      for (i = 0; i <= abs (n); i++)
	{
	  printf (" cxroot(z,%d,n) = \t", i);
	  xErrNo = 0;
	  w = cxroot (z, i, n);
	  switch (xErrNo)
	    {
	    case XENONE:
	      re = xpr_asprint (w.re, 1, 0, DECD);
	      im = xpr_asprint (w.im, 1, 0, DECD);
	      printf ("%s  \t%s\n", re, im);
	      free ((void *) re);
	      free ((void *) im);
	      break;
	    case XEDIV:
	      printf ("*** Division by zero\n");
	      break;
	    case XEDOM:
	      printf ("*** Out of domain\n");
	      break;
	    case XEBADEXP:
	      printf ("*** Bad exponent\n");
	      break;
	    case XFPOFLOW:
	      printf ("*** Floating point overflow\n");
	      break;
	    default:
	      printf ("*** Unknown error\n");
	    }
	}
    }
  fclose (fp);
  return 0;
}

